package com.company;

public class kalkulacka {

    public int a;
    public int b;

    public void scitaj(){
        int vysledok = a + b;
        System.out.println(vysledok);
    }
    public void odcitaj(){
        int vysledok = a - b;
        System.out.println(vysledok);
    }
    public void vynasob(){
        int vysledok = a * b;
        System.out.println(vysledok);
    }
    public void vydel(){
        int vysledok = a / b;
        System.out.println(vysledok);
    }
}
